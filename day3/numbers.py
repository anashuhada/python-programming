# find maximum of 3 numbers - max()
# max() - returns maximum value
print(max(10, 200, 30)) # 200
print(max(10.5, 20.5, 5.5)) # 20.5

# min() - returns minimum value
print(min(10, 200, 30)) # 200
print(min(10.5, 20.5, 5.5)) # 5.5

