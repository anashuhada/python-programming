# Concatenation +

print(10 + 10) # 20, valid
print(10.5 + 12.0) # 22.5, valid
print(10 + 10.5) # 20.5, valid

print('welcome' + 'python')

print(True + 5) # when True = 1, False = 0
print(False + 5)
print(True + True)

print(10 + 'welcome') # 2 different data type, invalid
print(10.5 + 'welcome') # 2 different data type, invalid
print(True + 'welcome') # 2 different data type, invalid